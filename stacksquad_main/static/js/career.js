// Stagger animate cards on load
window.addEventListener('DOMContentLoaded', function () {
    var cards = document.querySelectorAll('.job-card');
    cards.forEach(function (card, i) {
        setTimeout(function () {
            card.classList.add('show');
        }, 100 + i * 70);
    });
});

// Open modal
function openModal(key) {
    var j = jobs[key];
    if (!j) return;
    document.getElementById('mTitle').textContent = j.title;
    document.getElementById('mDesc').textContent = j.desc;
    var pills = document.getElementById('mPills');
    pills.innerHTML = j.pills.map(function (p) { return '<span class="pill">' + p + '</span>'; }).join('');
    var duties = document.getElementById('mDuties');
    duties.innerHTML = j.duties.map(function (d) { return '<li>' + d + '</li>'; }).join('');
    var reqs = document.getElementById('mReqs');
    reqs.innerHTML = j.reqs.map(function (r) { return '<li>' + r + '</li>'; }).join('');
    document.getElementById('overlay').classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    document.getElementById('overlay').classList.remove('open');
    document.body.style.overflow = '';
}

document.querySelectorAll('.job-card').forEach(function (card) {
    card.addEventListener('click', function () {
        openModal(card.getAttribute('data-key'));
    });
});

document.getElementById('mClose').addEventListener('click', closeModal);
document.getElementById('overlay').addEventListener('click', function (e) {
    if (e.target === this) closeModal();
});
document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') closeModal();
});
document.querySelectorAll('a[href="#"]').forEach(function (a) {
    a.addEventListener('click', function (e) { e.preventDefault(); });
});